package JAVA_Basics;

public class DataTypes {
    public static void main(String[] args){

        // Initialisation and giving values to the variables
        int a = 10;
        long b = 500000;
        short c = 5000;
        String d = "Ankit Singh";
        char e = 'A';
        float f = 0.215f;
        double g = 53103.1561;
        boolean h = true;

        // Printing all the variables
        System.out.println("this is int: " + a);
        System.out.println("this is long: " + b);
        System.out.println("this is short: " + c);
        System.out.println("this is String: " + d);
        System.out.println("this is char: " + e);
        System.out.println("this is float: " + f);
        System.out.println("this is double: " + g);
        System.out.println("this is boolean: " + h);
    }
}
